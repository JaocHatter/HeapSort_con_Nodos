    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        Integer N = modeloList1.size();//aquie esta los elementos a ordenar
        Integer idx;
        if(N<1) return;
        String tar = target.getText();
        SearchObject a = new SearchObject();
        if(age_button.isSelected()){
            Integer[] ordered_list = new Integer[N];
            //processing data   
            int it=0;
            for(Persona x: sortedList){
                ordered_list[it]=x.getAge();
                it++;
            }
            //Aplicar Algoritmo
            idx = a.Binaria(ordered_list,Integer.parseInt(tar));
        }else{
            String[] ordered_list =  new String[N];
            int it=0;
            for(Persona x: sortedList){
                ordered_list[it]=x.getName();
                it++;
            }
            idx = a.Binaria(ordered_list, tar);
        }
        if(idx==-1){
            JOptionPane.showMessageDialog(this, "No se encontró el objeto en la lista","ERROR", JOptionPane.WARNING_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Se encontró el objeto en el indice " + String.valueOf(idx),"ERROR", JOptionPane.WARNING_MESSAGE);
        }
        target.setText(CADENA_VACIA);
    }//GEN-LAST:event_SearchActionPerformed

