    private void Registrar(){
        String nombre=name_bar.getText();
        int edad=Integer.parseInt(age_bar.getText());
        if(edad<14 || edad>99)
        {
            JOptionPane.showMessageDialog(this, "Ingrese Edad mayor a 14 años","ERROR", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Persona p=new Persona(nombre,edad,'M');
        dir.add(p);
        modeloList1.addElement(p.toString());
        if (name_button.isSelected())
            sortedList=dir.sort(Persona.NAME,"HEAP");
        else
            sortedList=dir.sort(Persona.AGE,"HEAP");
        
        modeloList1.removeAllElements();
        for(Persona x: sortedList)
            modeloList1.addElement(x.toString());
        clearEntry();
        name_bar.requestFocus();
    }
    private void clearEntry(){
        name_bar.setText(CADENA_VACIA);
        age_bar.setText(CADENA_VACIA);
    }
